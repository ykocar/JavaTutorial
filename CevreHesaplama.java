
public class CevreHesaplama {

public static void main(String[] args) {
    double radius; // Declare radius
    double round; // Declare area

    // Assign a radius
    radius = 5.5; // New value is radius

    // Compute area
    round = 2 * radius * 3.14159;

    // Display results
    System.out.println("The round for the circle of radius " +
      radius + " is " + round);
  }
}
